package com.github.mikephil.charting.data

import com.github.mikephil.charting.components.YAxis
import com.github.mikephil.charting.highlight.Highlight
import com.google.common.truth.Truth.assertThat
import org.junit.Test

class BarDataTest : BarLineScatterCandleBubbleDataTest<BarData>() {
	@Test
	fun emptyDataSet() {
		this.data = BarData()

		val dataSet = BarDataSet(mutableListOf(), "q1 2018")
		val entry = BarEntry(1f, 2f)

		assertThat(this.data.dataSetCount).isEqualTo(0)
		assertThat(this.data.dataSets).isEmpty()
		assertThat(this.data.yMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.yMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.dataSetLabels).isEmpty()
		assertThat(this.data.getEntryForHighlight(Highlight(0f, 0f, 0))).isNull()
		assertThat(this.data.getDataSetByLabel("", false)).isNull()
		assertThat(this.data.getDataSetByLabel("", true)).isNull()
		assertThat(this.data.getDataSetByLabel("q1 2018", false)).isNull()
		assertThat(this.data.getDataSetByLabel("q1 2018", true)).isNull()
		assertThat(this.data.getDataSetByLabel("Q2 2018", false)).isNull()
		assertThat(this.data.getDataSetByLabel("Q2 2018", true)).isNull()
		assertThat(this.data.getDataSetByLabel("Q3 2018", false)).isNull()
		assertThat(this.data.getDataSetByLabel("Q3 2018", true)).isNull()
		assertThat(this.data.getDataSetByIndex(-1)).isNull()
		assertThat(this.data.getDataSetByIndex(0)).isNull()
		assertThat(this.data.getDataSetByIndex(1)).isNull()
		assertThat(this.data.getDataSetForEntry(null)).isNull()
		assertThat(this.data.getDataSetForEntry(entry)).isNull()
		assertThat(this.data.colors).isEmpty()
		assertThat(this.data.getIndexOfDataSet(dataSet)).isEqualTo(-1)
		assertThat(this.data.contains(dataSet)).isFalse()

		this.data.addDataSet(null)
		assertThat(this.data.dataSetCount).isEqualTo(0)

		this.data.addDataSet(dataSet)
		assertThat(this.data.dataSetCount).isEqualTo(1)
		assertThat(this.data.dataSets).containsExactly(dataSet)
		assertThat(this.data.yMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.yMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.dataSetLabels).asList().containsExactly("q1 2018")
		assertThat(this.data.getEntryForHighlight(Highlight(0f, 0f, 0))).isNull()
		assertThat(this.data.getDataSetByLabel("", false)).isNull()
		assertThat(this.data.getDataSetByLabel("", true)).isNull()
		assertThat(this.data.getDataSetByLabel("q1 2018", false)).isEqualTo(dataSet)
		assertThat(this.data.getDataSetByLabel("q1 2018", true)).isEqualTo(dataSet)
		assertThat(this.data.getDataSetByLabel("Q2 2018", false)).isNull()
		assertThat(this.data.getDataSetByLabel("Q2 2018", true)).isNull()
		assertThat(this.data.getDataSetByLabel("Q3 2018", false)).isNull()
		assertThat(this.data.getDataSetByLabel("Q3 2018", true)).isNull()
		assertThat(this.data.getDataSetByIndex(-1)).isNull()
		assertThat(this.data.getDataSetByIndex(0)).isEqualTo(dataSet)
		assertThat(this.data.getDataSetByIndex(1)).isNull()
		assertThat(this.data.colors).asList().containsExactly(0x8CEAFF)
		assertThat(this.data.getIndexOfDataSet(dataSet)).isEqualTo(0)
		assertThat(this.data.contains(dataSet)).isTrue()

		assertThat(this.data.removeDataSet(null)).isFalse()
		assertThat(this.data.removeDataSet(dataSet)).isTrue()
		assertThat(this.data.dataSetCount).isEqualTo(0)

		this.data.addDataSet(dataSet)
		assertThat(this.data.removeDataSet(1)).isFalse()
		assertThat(this.data.removeDataSet(0)).isTrue()

		this.data.addEntry(entry, -1)
		assertThat(this.data.yMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.yMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.removeEntry(entry, -1)).isFalse()

		this.data.addEntry(entry, 0)
		assertThat(this.data.yMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.yMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.removeEntry(entry, 0)).isFalse()

		this.data.addEntry(entry, 1)
		assertThat(this.data.yMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.yMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.removeEntry(entry, 1)).isFalse()

		this.data.addDataSet(dataSet)
		this.data.addEntry(entry, -1)
		assertThat(this.data.yMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.yMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.xMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mLeftAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.removeEntry(entry, -1)).isFalse()

		this.data.addEntry(entry, 0)
		assertThat(this.data.yMax).isEqualTo(2f)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(2f)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(2f)
		assertThat(this.data.yMin).isEqualTo(2f)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(2f)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(2f)
		assertThat(this.data.xMax).isEqualTo(1f)
		assertThat(this.data.xMin).isEqualTo(1f)
		assertThat(this.data.mLeftAxisMax).isEqualTo(2f)
		assertThat(this.data.mLeftAxisMin).isEqualTo(2f)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.removeEntry(entry, 0)).isTrue()
		assertThat(this.data.yMax).isEqualTo(2f)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(2f)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(2f)
		assertThat(this.data.yMin).isEqualTo(2f)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(2f)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(2f)
		assertThat(this.data.xMax).isEqualTo(1f)
		assertThat(this.data.xMin).isEqualTo(1f)
		assertThat(this.data.mLeftAxisMax).isEqualTo(2f)
		assertThat(this.data.mLeftAxisMin).isEqualTo(2f)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)

		this.data.addEntry(entry, 1)
		assertThat(this.data.yMax).isEqualTo(2f)
		assertThat(this.data.getYMax(YAxis.AxisDependency.LEFT)).isEqualTo(2f)
		assertThat(this.data.getYMax(YAxis.AxisDependency.RIGHT)).isEqualTo(2f)
		assertThat(this.data.yMin).isEqualTo(2f)
		assertThat(this.data.getYMin(YAxis.AxisDependency.LEFT)).isEqualTo(2f)
		assertThat(this.data.getYMin(YAxis.AxisDependency.RIGHT)).isEqualTo(2f)
		assertThat(this.data.xMax).isEqualTo(1f)
		assertThat(this.data.xMin).isEqualTo(1f)
		assertThat(this.data.mLeftAxisMax).isEqualTo(2f)
		assertThat(this.data.mLeftAxisMin).isEqualTo(2f)
		assertThat(this.data.mRightAxisMax).isEqualTo(-java.lang.Float.MAX_VALUE)
		assertThat(this.data.mRightAxisMin).isEqualTo(java.lang.Float.MAX_VALUE)
		assertThat(this.data.removeEntry(entry, -1)).isFalse()
	}
}
